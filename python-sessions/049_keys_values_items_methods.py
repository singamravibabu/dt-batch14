countries = {"IN":"India",
             "CA":"Canada",
             "UK":"United Kingdom",
             "US":"United States",
             "AU":"Australia"}

for k in countries:
    print(k, end=' ')

    

for k in countries.keys():
    print(k, end=' ')

    

for k in countries.values():
    print(k, end=' ')

    

for k in countries.items():
    print(k, end=' ')
