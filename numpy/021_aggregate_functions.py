import numpy as np

arr = np.array([1, 2, 3, 4, 5])
# Calculate the sum of all elements in the array
total_sum = np.sum(arr)
print(total_sum)  # Output: 15
# Calculate the mean of all elements in the array
mean_value = np.mean(arr)
print(mean_value)  # Output: 3.0
# Calculate the standard deviation of all elements in the array
std_dev = np.std(arr)
print(std_dev)  # Output: 1.4142135623730951
# Calculate the variance of all elements in the array
variance = np.var(arr)
print(variance)  # Output: 2.0
# Calculate the minimum value in the array
min_value = np.min(arr)
print(min_value)  # Output: 1
# Calculate the maximum value in the array
max_value = np.max(arr)
print(max_value)  # Output: 5
